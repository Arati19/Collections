package Collection_demo;

public class String_demo {
	
		
		public static void main(String [] args) {
			
			    String input = "ARATI"; 
			    
		        char[] a = input.toCharArray(); 
		        
		        for (int i = a.length-1; i>=0; i--) 
		            System.out.print(a[i]);  
			    
			}	
} 

